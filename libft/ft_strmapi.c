#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int				t;
	unsigned int	i;
	char			*str;

	if (!s)
		return (NULL);
	i = 0;
	t = ft_strlen(s);
	str = malloc(sizeof(char) * (t + 1));
	if (str == NULL)
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}	
